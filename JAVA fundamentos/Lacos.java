public class Lacos {
    
   public static void main(String[] args) {
    
        // √çndice
        int indice = 0;

        // While
        while(indice < 11){
            System.out.println(indice);
            indice++;
        }

        // Separar
        System.out.println("-------------------------");

        // Do While
        do{
            System.out.println(indice);
            indice--;
        }while(indice >= 0);

   }

}
